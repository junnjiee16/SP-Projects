-- calculate and insert rental fee and penalty fee for each transaction (i inserted all 1s to penalty and rental fee)
UPDATE p
SET p.rental_fee = calc.rental_fee, 
	p.penalty_fee = calc.penalty_fee
FROM payment_relation p
INNER JOIN (
	SELECT 
		p.transaction_id 'transaction_id',
		SUM((DATEDIFF(DAY, t.date_start, t.date_end)) * c.charge) 'rental_fee',
		SUM(
			CASE
				WHEN DATEDIFF(DAY, t.date_end, p.payment_date) <= 0 THEN 0
				WHEN c.category_id = 'C3' THEN (DATEDIFF(DAY, t.date_end, p.payment_date) * 0.5)
				ELSE (DATEDIFF(DAY, t.date_end, p.payment_date) * 1.5)
			END
		)'penalty_fee'
	FROM payment_relation p
	INNER JOIN transaction_relation t ON p.transaction_id = t.transaction_id
	INNER JOIN rent_reserve_relation r ON t.transaction_id = r.transaction_id
	INNER JOIN inventory_relation i ON r.unit_id = i.unit_id
	INNER JOIN catalog_relation c ON i.item_id = c.item_id
	GROUP BY p.transaction_id
) 
AS calc
ON p.transaction_id = calc.transaction_id